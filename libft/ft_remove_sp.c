#include "libft.h"

void	ft_remove_sp(char *str, char *buf)
{
	int		i;
	int		j;

	i = -1;
	j = -1;
	while (str[++i])
	{
		if (str[i] != ' ')
			buf[++j] = str[i];
	}
	buf[++j] = '\0';
}
