#include "libft.h"

int		ft_color_convert(int r, int g, int b)
{
	int		hex;

	if (r >= 0 && r <= 255)
	{
		hex = (r / 16) * pow(16, 5) + (r % 16) * pow(16, 4);
		if (g >= 0 && g <= 255)
		{
			hex += (g / 16) * pow(16, 3) + (g % 16) * pow(16, 2);
			if (b >= 0 && b <= 255)
			{
				hex += (b / 16) * 16 + (b % 16);
				return (hex);
			}
		}
	}
	return (-1);
}
